package Model;

import java.util.Date;

/**
 *
 * @author abraa
 */
public class MatchModel {
        
    private String team1;
    private String team2;
    private Date date;

    public MatchModel(String team1, String team2, Date date) {
        this.team1 = team1;
        this.team2 = team2;
        this.date = date;
    }

    public String getTeam1() {
        return team1;
    }

    public void setTeam1(String team1) {
        this.team1 = team1;
    }

    public String getTeam2() {
        return team2;
    }

    public void setTeam2(String team2) {
        this.team2 = team2;
    }

    public Date getDate() {
        return date;
    }

    public void setDate(Date date) {
        this.date = date;
    }
    
    
}
